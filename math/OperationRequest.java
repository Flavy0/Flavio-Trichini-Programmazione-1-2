package com.example.math;

public class OperationRequest {
    private String type;
    private int a;
    private int b;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}